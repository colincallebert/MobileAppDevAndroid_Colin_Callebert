package com.example.boerderij.ui.components

/**
 * Enum class representing different types of navigation styles in the application.
 */
enum class AppNavigationType {
    /**
     * Bottom navigation style where navigation options are displayed at the bottom of the screen.
     */
    BOTTOM_NAVIGATION,

    /**
     * Navigation rail style where navigation options are displayed in a vertical rail.
     */
    NAVIGATION_RAIL,

    /**
     * Permanent navigation drawer style where navigation options are permanently displayed in a drawer.
     */
    PERMANENT_NAVIGATION_DRAWER
}
